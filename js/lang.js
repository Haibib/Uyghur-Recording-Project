document.addEventListener('DOMContentLoaded', (event) => {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en';
    const buttons = document.querySelectorAll('.lang-option');
    buttons.forEach(button => {
        if (button.textContent.trim() === translations[savedLanguage].welcomeText.split(' ')[0]) {
            button.classList.add('selected');
        }
    });
    switchLanguage(savedLanguage, document.querySelector('.lang-option.selected'));
});

function switchLanguage(language, element) {
    localStorage.setItem('selectedLanguage', language);
    const translation = translations[language];
    
    document.getElementById('homepage-title').innerHTML = translations[language].homepagetitle;
    document.getElementById('paragraph-1').innerHTML = translations[language].paragraph1;
    document.getElementById('paragraph-2').innerHTML = translations[language].paragraph2;
    document.getElementById('paragraph-3').innerHTML = translations[language].paragraph3;
    document.getElementById('paragraph-4').innerHTML = translations[language].paragraph4;
    document.getElementById('contact').innerHTML = translations[language].contact;
    document.getElementById('c1-des').innerHTML = translations[language].c1;
    document.getElementById('c2-des').innerHTML = translations[language].c2;
    document.getElementById('c3-des').innerHTML = translations[language].c3;
    document.getElementById('c4-des').innerHTML = translations[language].c4;

    let textContainer = document.getElementById('main-text');
    textContainer.className = `text-container ${translation.direction === 'rtl' ? 'rtl' : ''}`;
    textContainer = document.getElementById('footer-text');
    textContainer.className = `text-container ${translation.direction === 'rtl' ? 'rtl' : ''}`;

    const buttons = document.querySelectorAll('.lang-option');
    buttons.forEach(button => {
        button.classList.remove('selected');
    });
    element.classList.add('selected');
}
